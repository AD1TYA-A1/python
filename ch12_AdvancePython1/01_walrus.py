# The walrus operator (:=), allows you to assign values to a varibale as a part of a exression. The operator, named for its resemblence to the eyes and tusk of walrus. 
# This is Officially called the "Assignment Expression"

# USING WALRUS OPERATOR
if (n:=len([1,2,5,6,4,34]))>3:
    print(f"Size of list is {n} and is greator than 3")


# HERE I HAVE ASSIGNED LEN OF A LIST TO A VARIABLE AND AT THE SAME TIME CHECKING THE IF CONDITION

# THIS OPERATOR ALLOWS US TO DO MULTIPLE THINGS IN ONE LINE 
# THIS IS BASICALLY A CONVIENIENCE METHOD TO DO TWO WORK IN A SINGLE PLACE